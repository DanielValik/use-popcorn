const KEY = "71b4a2a8";

export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState(tempWatchedData);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  useEffect(
    function () {
      async function getMoviesIMDBList() {
        try {
          setError("");
          setIsLoading(true);

          const responce = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`
          );
          if (!responce.ok)
            throw new Error("Something went wrong with fetching");

          const data = await responce.json();
          if (data.Response === "False") throw new Error("Movie not found");

          setMovies(data.Search);
        } catch (error) {
          console.error(error.message);
          setError(error.message);
        } finally {
          setIsLoading(false);
        }
      }

      if (query.length < 3) {
        setError("");
        setMovies([]);
        return;
      }

      getMoviesIMDBList();
    },
    [query]
  );

  function handleMovieSelection(id) {
    selectedId === id ? setSelectedId(null) : setSelectedId(id);
  }

  function handleMovieClose() {
    setSelectedId(null);
  }

  return (
    <>
      <Nav query={query} setQuery={setQuery}>
        <Results movies={movies} />
      </Nav>

      <Main>
        <Box>
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList
              movies={movies}
              handleMovieSelection={handleMovieSelection}
              handleMovieClose={handleMovieClose}
            />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>

        <Box>
          {selectedId ? (
            <SelectedMovie
              movieId={selectedId}
              KEY={KEY}
              handleMovieClose={handleMovieClose}
            />
          ) : (
            <>
              <Summary watched={watched} />
              <WatchedList watched={watched} />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

function Main({ children }) {
  return <main className="main">{children}</main>;
}

